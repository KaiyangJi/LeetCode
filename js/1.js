let arr = [
    { id: 1, name: '部门1', pid: 0 },
    { id: 2, name: '部门2', pid: 1 },
    { id: 3, name: '部门3', pid: 1 },
    { id: 4, name: '部门4', pid: 3 },
    { id: 5, name: '部门5', pid: 4 },
];
function transArrToTree(arr) {
    function pushChildren(obj) {
        obj.children = obj.children || [];
        for (var i = 0; i < arr.length; i++) {
            if (arr[i].pid === obj.id) {
                obj.children.push(pushChildren(arr[i]));
            }
        }

        console.log('obj', obj);

        return obj;
    }

    const result = pushChildren(arr[0]);
    return result;
}

transArrToTree(arr);
